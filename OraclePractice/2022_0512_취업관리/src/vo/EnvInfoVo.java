package vo;

public class EnvInfoVo {
	int env_idx;
	int salary;
	String overtime_pay;
	String bonus;
	String facility;
	int com_no;

	//getter/setter
	public int getEnv_idx() {
		return env_idx;
	}
	public void setEnv_idx(int env_idx) {
		this.env_idx = env_idx;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public String getOvertime_pay() {
		return overtime_pay;
	}
	public void setOvertime_pay(String overtime_pay) {
		this.overtime_pay = overtime_pay;
	}
	public String getBonus() {
		return bonus;
	}
	public void setBonus(String bonus) {
		this.bonus = bonus;
	}
	public String getFacility() {
		return facility;
	}
	public void setFacility(String facility) {
		this.facility = facility;
	}
	public int getCom_no() {
		return com_no;
	}
	public void setCom_no(int com_no) {
		this.com_no = com_no;
	}
	
	
	
}
